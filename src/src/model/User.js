class User {
    constructor(fullname, email, gender, phone, dob, address, role, userID, imageUrl) {
        this.Fullname = fullname;
        this.Email = email;
        this.Gender = gender;
        this.Phone = phone;
        this.DOB = dob;
        this.Address = address;
        this.Role = role;
        this.UserID = userID;
        this.ImageUrl = imageUrl;
    }
}
